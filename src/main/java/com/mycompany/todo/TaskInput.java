    private void submitBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitBtnActionPerformed

        tasks.add(nameTextField.getText());
        tasks.add(descTextField.getText());

        try {
            Date dated = jDateChooser3.getDate();
            if (dated != null) {
                SimpleDateFormat dateFormat = new SimpleDateFormat("dd-MM-yyyy");
                String dateStr = dateFormat.format(dated);
                tasks.add(dateStr);
            } else {
                tasks.add("null");
            }
        } catch (Exception e) {
            tasks.add("null");
            e.printStackTrace(); // Print the stack trace for debugging
        }
    
        
        LocalTime thime = timePicker.getTime();
        if(thime!=null){
        DateTimeFormatter mytime = DateTimeFormatter.ofPattern("HH:mm");
        String Time = mytime.format(thime);
        tasks.add(Time);
        } else {
            tasks.add("null");
        }
        MaintoDo.toDoFrame.addTask(tasks);
        this.dispose();
    }//GEN-LAST:event_submitBtnActionPerformed

