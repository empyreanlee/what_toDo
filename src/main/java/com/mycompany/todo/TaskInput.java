    private void dateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dateActionPerformed
        // TODO add your handling code here:
        String datestr = date.getText();
        SimpleDateFormat dateformat = new SimpleDateFormat("dd-MM-yyyy");
        
        try{
            Date date = dateformat.parse(datestr);
            LocalDate ldate = Instant.ofEpochMilli(date.getTime()).atZone(ZoneId.systemDefault()).toLocalDate();
            DayOfWeek dow = ldate.getDayOfWeek();
            Integer dom = ldate.getDayOfMonth();
            Month Month = ldate.getMonth();
            String month = Month.getDisplayName(TextStyle.FULL, Locale.ENGLISH);
            String formatted = dateformat.format(date);
            String dayofweek = dow.getDisplayName(TextStyle.SHORT, Locale.ENGLISH);
            jLabel1.setText(dayofweek +" "+ dom + ","+ month);        
        } catch(ParseException e){
            JOptionPane.showMessageDialog(this, "Invalid date");          
        }
    }//GEN-LAST:event_dateActionPerformed

    private void timeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_timeActionPerformed
        // TODO add your handling code here:
        String timestr = time.getText();
        String ampm = (String)jComboBox1.getSelectedItem();
        String combined = timestr + " " + ampm;
        DateTimeFormatter inputformat = DateTimeFormatter.ofPattern("hh:mm a");
        DateTimeFormatter outputformat = DateTimeFormatter.ofPattern("hh:mm a");        
        try{
            LocalTime timed = LocalTime.parse(combined, inputformat);
            String format = timed.format(outputformat);
            jLabel2.setText(format);
            
        } catch(DateTimeParseException e){
            JOptionPane.showMessageDialog(this, "Invalid ");
            
        }
    }//GEN-LAST:event_timeActionPerformed

